const color = "Blue";

switch (color) {
    case "green":
        console.log("You are green!");        
        break;
        case "black":
        console.log("You are black!");        
        break;
        case "yellow":
        console.log("You are yellow!");        
        break;
        case "Blue":
        console.log("You are Blue!");        
        break;

    default:
        console.log("You are white");
        break;
}